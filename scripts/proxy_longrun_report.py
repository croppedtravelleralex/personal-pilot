def collect_live_samples(
    args: argparse.Namespace,
) -> tuple[
    str,
    list[dict[str, object]],
    list[str],
    list[dict[str, object]],
    list[dict[str, object]],
    dict[str, object],
]:
    samples: list[dict[str, object]] = []
    errors: list[str] = []
    status_snapshots: list[dict[str, object]] = []
    fallback_mode = str(os.environ.get("PERSONA_PILOT_PROXY_MODE") or "")
    total_samples = max(args.samples, 1)
    for index in range(total_samples):
        try:
            status = fetch_status(args.base_url, args.api_key)
            status_snapshots.append(
                {
                    "captured_at": int(time.time()),
                    "status": status,
                }
            )
            samples.append(build_sample(status, fallback_mode=fallback_mode))
        except (urllib.error.URLError, TimeoutError, json.JSONDecodeError) as exc:
            errors.append(f"sample {index + 1}: {exc}")
        if index + 1 < total_samples:
            time.sleep(max(args.interval_seconds, 1))
    return args.base_url, samples, errors, [], status_snapshots, {}


def main() -> int:
    args = parse_args()
    if args.input_json:
        base_url, samples, errors, events, status_snapshots, run_context = load_samples_from_input(args.input_json)
        if args.base_url and args.base_url != "http://127.0.0.1:3000":
            base_url = args.base_url
    else:
        base_url, samples, errors, events, status_snapshots, run_context = collect_live_samples(args)

    summary = summarize(
        samples,
        errors,
        events,
        fallback_mode=str(run_context.get("mode") or os.environ.get("PERSONA_PILOT_PROXY_MODE") or ""),
    )
    source_summary = build_source_quality_summary(status_snapshots)
    summary["preset"] = str(run_context.get("preset") or "unknown")
    summary["stateful_followup_count"] = to_int(run_context.get("stateful_followup_count"))
    summary["sticky_stateful_region"] = run_context.get("sticky_stateful_region")
    summary["pool_hygiene_extra_args"] = list(run_context.get("pool_hygiene_extra_args") or [])
    session_summary = build_session_continuity_summary(summary)
    real_live_evidence_summary = build_real_live_evidence_summary(
        summary,
        session_summary,
        events,
    )
    payload = {
        "base_url": base_url,
        "generated_at": int(time.time()),
        "mode": summary.get("mode"),
        "preset": summary.get("preset"),
        "summary": summary,
        "source_quality_summary": source_summary,
        "session_continuity_summary": session_summary,
        "real_live_evidence_summary": real_live_evidence_summary,
        "samples": samples,
        "events": events,
    }
    gate_result = evaluate_report_payload(payload, Thresholds())
    summary["provider_supply_class"] = gate_result.provider_supply_class
    summary["strict_verdict"] = gate_result.strict_verdict
    summary["operational_verdict"] = gate_result.operational_verdict
    summary["provider_cap_reason"] = gate_result.provider_cap_reason
    payload["provider_supply_class"] = gate_result.provider_supply_class
    payload["strict_verdict"] = gate_result.strict_verdict
    payload["operational_verdict"] = gate_result.operational_verdict
    payload["provider_cap_reason"] = gate_result.provider_cap_reason
    payload["reason_code"] = gate_result.reason_code
    payload["failure_scope"] = gate_result.failure_scope
    payload["detail"] = gate_result.detail

    txt_output = Path(args.txt_output)
    json_output = Path(args.json_output)
    source_summary_txt_output = txt_output.parent / "source_quality_summary_latest.txt"
    source_summary_json_output = json_output.parent / "source_quality_summary_latest.json"
    session_summary_txt_output = txt_output.parent / "session_continuity_summary_latest.txt"
    session_summary_json_output = json_output.parent / "session_continuity_summary_latest.json"
    real_live_evidence_txt_output = txt_output.parent / "real_live_evidence_summary_latest.txt"
    real_live_evidence_json_output = json_output.parent / "real_live_evidence_summary_latest.json"
    txt_output.parent.mkdir(parents=True, exist_ok=True)
    json_output.parent.mkdir(parents=True, exist_ok=True)
    txt_output.write_text(render_text(base_url, summary, samples, events), encoding="utf-8")
    json_output.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    source_summary_txt_output.write_text(render_source_quality_text(source_summary), encoding="utf-8")
    source_summary_json_output.write_text(
        json.dumps(source_summary, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    session_summary_txt_output.write_text(render_session_continuity_text(session_summary), encoding="utf-8")
    session_summary_json_output.write_text(
        json.dumps(session_summary, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    real_live_evidence_txt_output.write_text(
        render_real_live_evidence_text(real_live_evidence_summary),
        encoding="utf-8",
    )
    real_live_evidence_json_output.write_text(
        json.dumps(real_live_evidence_summary, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(txt_output.read_text(encoding="utf-8"), end="")
    return 0 if samples else 1


