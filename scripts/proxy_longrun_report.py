def load_samples_from_input(
    input_path: str,
) -> tuple[
    str,
    list[dict[str, object]],
    list[str],
    list[dict[str, object]],
    list[dict[str, object]],
    dict[str, object],
]:
    payload = json.loads(Path(input_path).read_text(encoding="utf-8"))
    base_url = str(payload.get("base_url") or "http://127.0.0.1:3000")
    events = list(payload.get("events") or [])
    errors = [str(item) for item in (payload.get("errors") or [])]
    run_context = {
        "mode": payload.get("mode"),
        "preset": payload.get("preset"),
        "stateful_followup_count": payload.get("stateful_followup_count"),
        "sticky_stateful_region": payload.get("sticky_stateful_region"),
        "pool_hygiene_extra_args": list(payload.get("pool_hygiene_extra_args") or []),
    }

    raw_samples = list(payload.get("samples") or [])
    snapshots = list(payload.get("status_snapshots") or [])
    if raw_samples:
        return base_url, raw_samples, errors, events, snapshots, run_context

    samples = [
        build_sample(
            dict(item.get("status") or {}),
            captured_at=to_int(item.get("captured_at"), int(time.time())),
            fallback_mode=str(run_context.get("mode") or ""),
        )
        for item in snapshots
    ]
    return base_url, samples, errors, events, snapshots, run_context


