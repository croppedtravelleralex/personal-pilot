def summarize_geo_quality_from_sources(source_summaries: list[dict[str, object]]) -> dict[str, object]:
    declared_counts: dict[str, int] = {}
    effective_counts: dict[str, int] = {}
    active_share_by_effective: dict[str, float] = {}
    coverage_values: list[float] = []
    for item in source_summaries:
        declared_key = str(
            item.get("declared_geo_quality") or item.get("expected_geo_quality") or ""
        ).strip() or "unknown"
        effective_key = str(item.get("effective_geo_quality") or "").strip() or "unknown"
        declared_counts[declared_key] = declared_counts.get(declared_key, 0) + 1
        effective_counts[effective_key] = effective_counts.get(effective_key, 0) + 1
        active_share_by_effective[effective_key] = active_share_by_effective.get(effective_key, 0.0) + to_float(
            item.get("active_share_percent")
        )
        coverage_values.append(round(to_float(item.get("geo_coverage_percent")), 4))
    avg_geo_coverage_percent = round(
        sum(coverage_values) / len(coverage_values),
        4,
    ) if coverage_values else 0.0
    return {
        "declared_geo_quality_counts": dict(
            sorted(declared_counts.items(), key=lambda item: (-item[1], item[0]))
        ),
        "effective_geo_quality_counts": dict(
            sorted(effective_counts.items(), key=lambda item: (-item[1], item[0]))
        ),
        "externally_verified_source_count": to_int(effective_counts.get("externally_verified")),
        "host_geo_inferred_source_count": to_int(effective_counts.get("host_geo_inferred")),
        "mixed_degraded_source_count": to_int(effective_counts.get("mixed_degraded")),
        "unknown_geo_source_count": to_int(effective_counts.get("unknown")),
        "avg_geo_coverage_percent": avg_geo_coverage_percent,
        "median_geo_coverage_percent": median_or_zero(coverage_values),
        "max_geo_coverage_percent": max(coverage_values) if coverage_values else 0.0,
        "active_share_percent_by_effective_geo_quality": {
            key: round(value, 2)
            for key, value in sorted(
                active_share_by_effective.items(),
                key=lambda item: (-item[1], item[0]),
            )
        },
    }


def build_source_quality_summary(status_snapshots: list[dict[str, object]]) -> dict[str, object]:
    latest_status = dict((status_snapshots[-1] or {}).get("status") or {}) if status_snapshots else {}
    harvest = dict(latest_status.get("proxy_harvest_metrics") or {})
    source_summaries = [
        dict(item)
        for item in (harvest.get("source_summaries") or [])
        if isinstance(item, dict)
    ]
    source_summaries.sort(
        key=lambda item: (
            -to_int(item.get("active_count")),
            -to_float(item.get("promotion_rate")),
            -to_float(item.get("health_score")),
            str(item.get("source_label") or ""),
        )
    )
    low_promotion_threshold = 0.05
    quarantined_count = sum(1 for item in source_summaries if str(item.get("quarantine_until") or "").strip())
    healthy_count = sum(1 for item in source_summaries if to_float(item.get("health_score")) >= 60.0)
    active_source_count = sum(1 for item in source_summaries if to_int(item.get("active_count")) > 0)
